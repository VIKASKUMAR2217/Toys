void HAL_SYSTICK_Callback(void)
{
	TouchCount++;
	TouchInterface();
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
		if(HAL_GPIO_ReadPin(B1_GPIO_Port,B1_Pin) == GPIO_PIN_RESET)
		{
			debounceHandle = 1;
			HAL_TIM_Base_Stop_IT(&htim1);
		}
}

uint32_t GetTouchTime(void)
{
	return TouchCount;
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if(GPIO_Pin == B1_Pin)
	{
		if(HAL_GPIO_ReadPin(B1_GPIO_Port,B1_Pin))
		{
				ButtonRealeasedCallback();
		}
		else			
		{
				HAL_TIM_Base_Start_IT(&htim1);
				if (debounceHandle == 1)
				{
					ButtonPressedCallback();
					debounceHandle = 0;
				}
		}
	}
	__HAL_GPIO_EXTI_CLEAR_IT(GPIO_Pin);
}

void ButtonPressedCallback(void)
{
	StartTime = GetTouchTime();
	if(ButtonRealeasedFlag == 0)
		ButtonPressedFlag = 1;
}

void ButtonRealeasedCallback(void)
{
	EndTime = GetTouchTime();
	if(ButtonPressedFlag)
		ButtonRealeasedFlag = 1;
}

void TouchInterface(void)
{
		if(ButtonPressedFlag && ButtonRealeasedFlag)
		{
			touchType = (EndTime - StartTime);
			
			ButtonPressedFlag = 0;
			ButtonRealeasedFlag = 0;
			
			if(touchType >= 70 && touchType <= 500)
			{
				SingleTouch();
			}
			else if(touchType > 500 && touchType <= 1500)
			{
				LongTouch();
			}
			else if(touchType > 1500 && touchType <= 2500)
			{
				VeryLongTouch();
			}
			else
			{
				ButtonPressedFlag = 0;
				ButtonRealeasedFlag = 0;
				debounceHandle = 0;
				touchType = 0;
				HAL_UART_Transmit(&huart1,(uint8_t *) "ET\r\r\n",6, HAL_MAX_DELAY);
			}
		}
}


void SingleTouch(void)
{
	HAL_UART_Transmit(&huart1,(uint8_t *) "ST\r\r\n",6, HAL_MAX_DELAY);
	ButtonPressedFlag = 0;
	ButtonRealeasedFlag = 0;
}

void LongTouch(void)
{
	HAL_UART_Transmit(&huart1,(uint8_t *) "LT\r\r\n", 6, HAL_MAX_DELAY);
	ButtonPressedFlag = 0;
	ButtonRealeasedFlag = 0;
}

void VeryLongTouch(void)
{
	HAL_UART_Transmit(&huart1,(uint8_t *) "VLT\r\r\n",7, HAL_MAX_DELAY);
	ButtonPressedFlag = 0;
	ButtonRealeasedFlag = 0;
}

